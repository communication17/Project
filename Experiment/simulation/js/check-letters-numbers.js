function alphanumeric(inputtxt)
{ 
var letters = /^[/meil/]+$/;
if(inputtxt.value.match(letters))
{
alert('Your Input is correct : you can try another');
document.form1.text1.focus();
return true;
}
else
{
alert('Your Input is not valid please try another');
return false;
}
}
